info_powloki = '''
Elementy  złączne  do  konstrukcji  stalowych  naj
częściej  cynkuje  się  zanurzeniowo  (”ogniowo”)  
według  PN-EN  ISO  10684  lub  PN-EN  ISO  1461.  
Trwałość  powłok  cynkowanych  ogniowo  zależy  
między  innymi  od  obciążenia  korozyjnego  środo
wiska. Norma PN-EN ISO 14713 podaje 5 kategorii 
odporności  korozyjnej  od  C1  do  C5.  Dla  kategorii  
C1  (bardzo  słaba)  roczny  ubytek  powłoki  wyno
si  poniżej  0,1μm,  a  trwałość  powłoki  do  pierwszej
konserwacji,  przy  grubości  średniej  powłoki  25-85  
μm,  wynosi  co  najmniej  100-200  lat.  Dla  katego
rii  C5  (bardzo  silna)  roczny  ubytek  wynosi  6-8  μm,  
a trwałość powłoki do pierwszej konserwacji, przy 
grubości średniej powłoki 70-85 μm, wynosi 5-10 lat.
Znacznie  mniej  popularny,  ze  względu  na  niską  
odporność korozyjną jest ocynk galwaniczny (elek
trolityczny) według PN-EN ISO 4042. Coraz szersze 
zastosowanie mają powłoki cynku płatkowego (la
melowego) wg PN-EN ISO 10683 – powszechnie sto
sowane w motoryzacji, z uwagi na bardzo wysoką 
odporność  korozyjną,  wyższą  przy  zdecydowanie  
niższej grubości, niż powłoki z ocynku ogniowego.
Grupa  ASMET  posiada  dwa  zakłady  nanoszenia  
powłok  cynkowych  dzięki  czemu  może  zapewnić  
odpowiednią jakość powłok dla elementów składo
wych zestawów (śruba-nakrętka-podkładka) stoso
wanych w budownictwie.
'''
